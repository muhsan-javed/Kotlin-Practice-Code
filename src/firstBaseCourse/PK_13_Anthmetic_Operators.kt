fun main() {
    var firstNumber = 12.0
    var secondNumber = 7.0
    var result: Double
    result = firstNumber + secondNumber
    println("firstNo + SecondNo = $result")

    result = firstNumber - secondNumber
    println("firstNo - SecondNo = $result")

    result = firstNumber * secondNumber
    println("firstNo * SecondNo = $result")

    result = firstNumber / secondNumber
    println("firstNo / SecondNo = $result")

    result = firstNumber % secondNumber
    println("firstNo % SecondNo = $result")
}