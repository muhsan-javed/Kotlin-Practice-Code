package KotlinBasicCourse

fun main() {

    val byte : Byte = 127
    val short : Short = 28436
    val int : Int = 50424
    val long : Long = 984365231

    val pi: Float = 3.14F
    println("Float : $pi")

    val double : Double = 1.4548548545
    println("Double : $double")

    val char: Char = 'S'
    println("Char : $char")

    val string :String  = "Muhammad"
    println("String : $string")

    val name = "Ali"
    println("Name : $name")

    val boolean:Boolean = false
    println("Boolean : $boolean")



}